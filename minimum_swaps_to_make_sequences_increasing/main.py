#  dynamic_programming


class Solution:
    def minSwap(self, A, B):
        """
        :type A: List[int]
        :type B: List[int]
        :rtype: int
        """
        swap, fixed = 1, 0

        for i in range(1, len(A)):
            if A[i - 1] >= B[i] or B[i - 1] >= A[i]:
                swap += 1
            elif A[i - 1] >= A[i] or B[i - 1] >= B[i]:
                swap, fixed = fixed + 1, swap
            else:
                m = min(swap, fixed)
                swap = m + 1
                fixed = m

        return min(swap, fixed)


